%%6.2.3使用工作空间变量作为系统输入信号
t = 0:0.1:10;
x = sin(t);
input = [t',x'];      %t'是t的转置
%[t', x'] 把两个列向量左右拼接，得到一个 101×2 的矩阵：第 1 列是时间，
% 第 2 列是对应时刻的信号值 sin(t)。每一行就是"某个时刻 + 该时刻的输入值"。
% Simulink 对这种输入变量的格式有硬性规定：
% [时间列, 信号1列, 信号2列, ...]
% 时间必须是递增的列向量，信号值按列跟在后面。
load_system('ep_6_2_3')
set_param('ep_6_2_3/Gain','Gain','a+3')
a = 1;
simOut = sim('ep_6_2_3')
open_system('ep_6_2_3/Scope')