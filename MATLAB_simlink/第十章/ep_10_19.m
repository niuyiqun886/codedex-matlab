%%ss2ss函数，该函数可以根据指定变换矩阵对状态空间模型进行线性变换。
%%基本语法：sysT = ss2ss(sys,T)
%%例10-19：将例10-18中的系统模型通过下面变换矩阵进行变换：
%%T = [0 -1 0; 1 1 1; 0 2 1]
T = [0 -1 0; 1 1 1; 0 2 1];
%%A, B, C, D来自例10-18
A = [1 -3 -2; -1 1 -1; 2 4 5];
B = [1;1;0];
C = [0 1 0];
D = 0;
sys = ss(A, B, C, D);
sys1 = ss2ss(sys, T)
%[As, Bs, Cs, Ds] = ss2ss(A, B, C, D, T)        %ss2ss函数的另一种用法