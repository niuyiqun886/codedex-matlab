% 直接就是特征多项式的系数 -a1，...-an，做状态反馈u = -K·x 时，闭环矩阵A - BK只改变
% 这一行系数。也就是：
% 想把开环极点配置到哪里→写出期望的多项式→系数直接相减就得到K
% 极点配置在这个形式下是纯代数。无需解方程
