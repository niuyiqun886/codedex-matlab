%%例10-17已知一控制系统的状态空间表示为：
%%x'=[0 0 1;1 1 -2; 0 1 -3]x + [1;0;0]u
%%y=[1 1 1]x
%%求对角标准型和可控标准型
A = [0 0 1;1 1 -2; 0 1 -3];
B = [1;0;0];
C = [1 1 1];
D = 0;
[Ac,Bc,Cc,Dc,Pc] = canon(A,B,C,D,'modal')         %对角标准型
[AS,Bs,Cs,Ds,Ps] = canon(A,B,C,D,'companion')     %可控标准型

%%对角标准型(modal form)：
%%用途：
% 1.解耦：对角化后每个状态方程变为：
% $\dot{\bar{x_{i}}} = \lambda_{i}\cdot \bar{x_{i}} + \beta_{i}\cdot \mu $
% 互不耦合。系统响应就是各模态$e^{\lambda_{i}\cdot t}$的叠加，每个特征根对应一个
% 独立的"模态"。哪个模态快、哪个慢、哪个振荡、一目了然。

% 2.判断可控/可观性最直观：对角形式下，如果$\bar{B}$的第i行为0，第i个模态
% 就不可控；$\bar{C}$的第i列为零则不可观。这比算可控矩阵的秩直观的多--可以直接
% 看出"哪个模态出现了问题"。

% 3.数值计算和降阶：对角(或块对角)形式数值良好，模态之间独立，做模型降阶时可直
% 砍掉不重要的模态



